package l05;

public class Unary_02 {

	public static void main(String[] args) 
	{
		//increment[++a(pre-increment),a++(post-increment)]
		     int a=25;
		     System.out.println(a);
		     System.out.println(++a);
		     System.out.println(a++);
		     System.out.println(a);
		     System.out.println();
		     int d=25;
		     System.out.println();
		     System.out.println(d);
		 	 System.out.println(--d);
		 	 System.out.println(d--);
		 	 System.out.println(d);
	}

}
