package l05;

public class Relational_03 {

	public static void main(String[] args) 
	{
		  int a=3;
		  int b=8;
		  System.out.println(a==b);
		  System.out.println(">");
		  System.out.println(a!=b);
		  System.out.println(">");
		  System.out.println(a>b);
		  System.out.println(">");
		  System.out.println(a<b);
		  System.out.println(">");
		  System.out.println(a>=b);
		  System.out.println(">");
		  System.out.println(a<=b);
	}

}
